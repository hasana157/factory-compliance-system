"""scripts/analyze_results.py

Accuracy analysis script for FactoryGuard batch inference results.

Reads results/batch_report.json and compares detection outputs against
the dataset's folder-label ground truth (Safe vs. violation class).

Outputs:
  - Confusion matrix per class (Precision, Recall, F1)
  - results/performance_metrics.md (human-readable report)

Usage:
    python scripts/analyze_results.py
    python scripts/analyze_results.py --report results/batch_report.json --output results/performance_metrics.md
"""

from __future__ import annotations

import argparse
import json
import sys
from collections import defaultdict
from pathlib import Path

_ROOT = Path(__file__).resolve().parent.parent
if str(_ROOT) not in sys.path:
    sys.path.insert(0, str(_ROOT))

VIOLATION_BEHAVIORS = {
    "Safe_Walkway_Violation",
    "Unauthorized_Intervention",
    "Opened_Panel_Cover",
    "Carrying_Overload_with_Forklift",
}

SAFE_LABELS = {"SAFE", "Normal", "Safe", "safe", "normal"}


def _is_ground_truth_violation(folder_label: str) -> bool:
    return folder_label not in SAFE_LABELS


def _get_detected_behaviors(result: dict) -> set[str]:
    return {v["behavior_class"] for v in result.get("violations", [])}


def analyze(report_path: Path) -> dict:
    with report_path.open("r", encoding="utf-8") as fh:
        report = json.load(fh)

    results = report["results"]

    # --- Binary (violation / safe) confusion matrix ---
    binary = {"TP": 0, "FP": 0, "FN": 0, "TN": 0}
    for r in results:
        gt_violation  = _is_ground_truth_violation(r["folder_label"])
        det_violation = r.get("detected", False)

        if gt_violation and det_violation:
            binary["TP"] += 1
        elif not gt_violation and det_violation:
            binary["FP"] += 1
        elif gt_violation and not det_violation:
            binary["FN"] += 1
        else:
            binary["TN"] += 1

    # --- Per-class confusion matrices ---
    per_class: dict[str, dict[str, int]] = defaultdict(
        lambda: {"TP": 0, "FP": 0, "FN": 0, "TN": 0}
    )
    for r in results:
        gt_label    = r["folder_label"]
        gt_behavior = gt_label if gt_label in VIOLATION_BEHAVIORS else None
        detected    = _get_detected_behaviors(r)

        for behavior in VIOLATION_BEHAVIORS:
            gt_positive  = behavior == gt_behavior
            det_positive = behavior in detected
            if gt_positive and det_positive:
                per_class[behavior]["TP"] += 1
            elif not gt_positive and det_positive:
                per_class[behavior]["FP"] += 1
            elif gt_positive and not det_positive:
                per_class[behavior]["FN"] += 1
            else:
                per_class[behavior]["TN"] += 1

    return {
        "binary":    binary,
        "per_class": dict(per_class),
        "metadata":  report["metadata"],
    }


def _safe_div(a: int, b: int) -> float:
    return a / b if b else 0.0


def _metrics(cm: dict) -> dict:
    tp, fp, fn = cm["TP"], cm["FP"], cm["FN"]
    precision = _safe_div(tp, tp + fp)
    recall    = _safe_div(tp, tp + fn)
    f1        = _safe_div(2 * precision * recall, precision + recall)
    return {"precision": precision, "recall": recall, "f1": f1}


def _bar(value: float, width: int = 20) -> str:
    filled = int(round(value * width))
    return "█" * filled + "░" * (width - filled)


def render_markdown(analysis: dict, output_path: Path) -> str:
    meta  = analysis["metadata"]
    bin_  = analysis["binary"]
    pc    = analysis["per_class"]

    bin_m = _metrics(bin_)

    lines = [
        "# FactoryGuard — Batch Inference Performance Metrics",
        "",
        "> Generated by `scripts/analyze_results.py`",
        "> Dataset ground truth: folder-label names (Safe vs. violation class).",
        "> Detection: pixel-only heuristics — no path reading.",
        "",
        "---",
        "",
        "## Dataset Summary",
        "",
        f"| Metric | Value |",
        f"|--------|-------|",
        f"| Total videos processed | {meta['total_videos']} |",
        f"| Videos with violations detected | {meta['detected']} |",
        f"| Videos classified safe | {meta['safe']} |",
        f"| Processing errors | {meta['errors']} |",
        f"| Overall detection rate | {meta['detection_rate']}% |",
        "",
        "---",
        "",
        "## Binary Classification (Violation vs. Safe)",
        "",
        "| | Predicted Violation | Predicted Safe |",
        "|---|---|---|",
        f"| **Ground Truth Violation** | {bin_['TP']} TP | {bin_['FN']} FN |",
        f"| **Ground Truth Safe** | {bin_['FP']} FP | {bin_['TN']} TN |",
        "",
        f"| Metric | Score | Bar |",
        f"|--------|-------|-----|",
        f"| Precision | {bin_m['precision']:.2%} | {_bar(bin_m['precision'])} |",
        f"| Recall    | {bin_m['recall']:.2%} | {_bar(bin_m['recall'])} |",
        f"| F1 Score  | {bin_m['f1']:.2%} | {_bar(bin_m['f1'])} |",
        "",
        "---",
        "",
        "## Per-Class Performance",
        "",
        "| Violation Class | TP | FP | FN | TN | Precision | Recall | F1 |",
        "|---|---|---|---|---|---|---|---|",
    ]

    for behavior in sorted(pc.keys()):
        cm = pc[behavior]
        m  = _metrics(cm)
        display = behavior.replace("_", " ")
        lines.append(
            f"| {display} | {cm['TP']} | {cm['FP']} | {cm['FN']} | {cm['TN']} "
            f"| {m['precision']:.1%} | {m['recall']:.1%} | {m['f1']:.1%} |"
        )

    lines += [
        "",
        "---",
        "",
        "## Severity Rationale",
        "",
        "Severity tiers are derived directly from the PDF policy callout language:",
        "",
        "| Policy Callout | Default Severity | Behavior |",
        "|---|---|---|",
        "| WARNING | MEDIUM | Safe_Walkway_Violation, Opened_Panel_Cover |",
        "| CRITICAL SAFETY NOTICE | HIGH | Unauthorized_Intervention, Carrying_Overload_with_Forklift |",
        "",
        "> Walkway violations default to **MEDIUM** because the policy places them under a *'WARNING'* callout.",
        "> Unauthorized interventions default to **HIGH** because they are under a *'CRITICAL SAFETY NOTICE'*.",
        "",
        "---",
        "",
        "## Detection Method Summary",
        "",
        "| Violation | CV Method | Notes |",
        "|---|---|---|",
        "| Safe Walkway Violation | YOLO person detection + HSV green foot-region ratio | Threshold from auto_generated_rules.json |",
        "| Unauthorized Intervention | YOLO person detection + upper-torso HSV vest ratio | Threshold from auto_generated_rules.json |",
        "| Opened Panel Cover | Edge-density Canny classifier on panel region | Threshold from auto_generated_rules.json |",
        "| Carrying Overload with Forklift | YOLO truck detection + contour block counter on fork region | max_blocks from auto_generated_rules.json |",
        "",
        "> All thresholds dynamically loaded from `src/severity/auto_generated_rules.json`.",
        "> No hardcoded values in detection logic.",
        "",
    ]

    md = "\n".join(lines)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(md, encoding="utf-8")
    print(f"[analyze] ✅ Metrics saved to: {output_path}")
    return md


def _cli() -> None:
    parser = argparse.ArgumentParser(
        description="Analyze batch inference results and produce performance_metrics.md"
    )
    parser.add_argument(
        "--report",
        default=str(_ROOT / "results" / "batch_report.json"),
        help="Path to batch_report.json produced by batch_inference.py",
    )
    parser.add_argument(
        "--output",
        default=str(_ROOT / "results" / "performance_metrics.md"),
        help="Output path for performance_metrics.md",
    )
    args = parser.parse_args()

    report_path = Path(args.report)
    if not report_path.exists():
        print(f"ERROR: batch report not found at {report_path}")
        print("Run batch_inference.py first.")
        sys.exit(1)

    analysis = analyze(report_path)
    md = render_markdown(analysis, Path(args.output))
    print("\nPreview:")
    print(md[:2000])


if __name__ == "__main__":
    _cli()
